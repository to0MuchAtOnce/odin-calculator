function add(num1, num2) {
  return num1 + num2;
}
console.log(add(2, 5));

function subtract(num1, num2) {
  return num1 - num2;
}
console.log(subtract(9, 6));

function multiply(num1, num2) {
  return num1 * num2;
}
console.log(multiply(2, 2));

function divide(num1, num2) {
  return num1 / num2;
}
console.log(divide(10, 2));
